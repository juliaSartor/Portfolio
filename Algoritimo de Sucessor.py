#Faça um algoritimo que calcule o antecessor de um número

Número = int(input('Escreva um número:'))
Ant = Número-1
Suc = Número+1
Quad = Número**2
Met = Número/2
Dob = Número*2
print(f'Seu antecessor é: {Ant}')
print(f'Seu sucessor é: {Suc}')
print(f'Seu dobro é: {Dob}')
print(f'Sua metade é: {Met}')
print(f'Seu quadrado é: {Quad}')
